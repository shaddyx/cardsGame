package ua.org.shaddy.cardsgame.game.entities;

public class JScript {
	final String script;
	final String name;
	public JScript(String script, String name) {
		super();
		this.script = script;
		this.name = name;
	}
	public String getScript() {
		return script;
	}
	public String getName() {
		return name;
	}	
}
